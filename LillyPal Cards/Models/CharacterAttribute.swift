// MARK: - Enums

// MARK: - Enum: CharacterAttribute

/// This enum contains cases for each character attribute.
enum CharacterAttribute {
 
    /// This case enumerates a character's 'body' attribute, accessed via the `body` property in types conforming to the `Character` protocol.
    case body
    
    /// This case enumerates a character's 'mind' attribute, accessed via the `mind` property in types conforming to the `Character` protocol.
    case mind
    
    /// This case enumerates a character's 'social' attribute, accessed via the `social` property in types conforming to the `Character` protocol.
    case social
}
